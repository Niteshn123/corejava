package Constructior;

public class Const {
	
	private int id;
	private String name;
	
	public Const() {
		System.out.println("I am default");
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
